import AVFoundation
import Foundation
import UserNotifications
import WatchKit

@MainActor
final class ScheduleManager: ObservableObject {
    struct UpcomingService: Identifiable {
        let mass: MassTime
        let date: Date

        var id: UUID { mass.id }
    }

    @Published private(set) var schedule: ParishSchedule {
        didSet {
            scheduleStore.reset(to: schedule)
            refreshUpcomingService()
        }
    }

    @Published private(set) var nextService: UpcomingService?
    @Published var isAngelusEnabled: Bool {
        didSet { defaults.set(isAngelusEnabled, forKey: Self.angelusDefaultsKey) }
    }

    @Published var hasCompletedOnboarding: Bool {
        didSet {
            defaults.set(hasCompletedOnboarding, forKey: Self.onboardingDefaultsKey)
            if hasCompletedOnboarding {
                isOnboardingPresented = false
            }
        }
    }

    @Published var isOnboardingPresented: Bool
    @Published private(set) var notificationStatus: UNAuthorizationStatus = .notDetermined

    private let scheduleStore: ParishScheduleStore
    private let defaults: UserDefaults
    private let calendar: Calendar
    private var audioPlayer: AVAudioPlayer?

    private static let angelusDefaultsKey = "isAngelusEnabled"
    private static let onboardingDefaultsKey = "hasCompletedOnboarding"

    init(
        scheduleStore: ParishScheduleStore = ParishScheduleStore(),
        defaults: UserDefaults = .standard,
        calendar: Calendar = .current
    ) {
        self.scheduleStore = scheduleStore
        self.defaults = defaults
        self.calendar = calendar
        let persistedSchedule = scheduleStore.schedule
        self.schedule = persistedSchedule
        self.isAngelusEnabled = defaults.object(forKey: Self.angelusDefaultsKey) as? Bool ?? true
        self.hasCompletedOnboarding = defaults.object(forKey: Self.onboardingDefaultsKey) as? Bool ?? false
        self.isOnboardingPresented = !hasCompletedOnboarding
        refreshUpcomingService()
        refreshNotificationStatus()
    }

    // MARK: - Schedule Management

    func applyPreset(_ preset: SchedulePreset) {
        schedule = ParishSchedule(parishName: preset.title, masses: sortedMasses(preset.masses))
    }

    func updateMasses(_ masses: [MassTime]) {
        schedule.masses = sortedMasses(masses)
    }

    func updateParishName(_ name: String) {
        schedule.parishName = name
    }

    func refreshUpcomingService(referenceDate: Date = Date()) {
        nextService = Self.calculateNextService(for: schedule, after: referenceDate, calendar: calendar)
    }

    private func sortedMasses(_ masses: [MassTime]) -> [MassTime] {
        masses.sorted { lhs, rhs in
            if lhs.weekday.rawValue == rhs.weekday.rawValue {
                let lhsHour = lhs.time.hour ?? 0
                let rhsHour = rhs.time.hour ?? 0
                if lhsHour == rhsHour {
                    return (lhs.time.minute ?? 0) < (rhs.time.minute ?? 0)
                }
                return lhsHour < rhsHour
            }
            return lhs.weekday.rawValue < rhs.weekday.rawValue
        }
    }

    private static func calculateNextService(
        for schedule: ParishSchedule,
        after date: Date,
        calendar: Calendar
    ) -> UpcomingService? {
        let services = schedule.masses.compactMap { mass -> UpcomingService? in
            var components = DateComponents()
            components.weekday = mass.weekday.rawValue
            components.hour = mass.time.hour
            components.minute = mass.time.minute
            components.second = mass.time.second ?? 0

            guard let nextDate = calendar.nextDate(
                after: date,
                matching: components,
                matchingPolicy: .nextTimePreservingSmallerComponents,
                direction: .forward
            ) else {
                return nil
            }

            return UpcomingService(mass: mass, date: nextDate)
        }

        return services.min { $0.date < $1.date }
    }

    // MARK: - Onboarding

    func completeOnboarding() {
        hasCompletedOnboarding = true
    }

    // MARK: - Notifications

    func refreshNotificationStatus() {
        let center = UNUserNotificationCenter.current()
        center.getNotificationSettings { settings in
            Task { @MainActor in
                self.notificationStatus = settings.authorizationStatus
            }
        }
    }

    func requestNotificationPermissions() {
        let center = UNUserNotificationCenter.current()
        center.requestAuthorization(options: [.alert, .sound]) { _, _ in
            self.refreshNotificationStatus()
        }
    }

    // MARK: - Bell Control

    func ringBellNow() {
        playHaptic()
        playAudio()
    }

    private func playHaptic() {
        #if os(watchOS)
        WKInterfaceDevice.current().play(.notification)
        #endif
    }

    private func playAudio() {
        prepareAudioPlayerIfNeeded()
        audioPlayer?.currentTime = 0
        audioPlayer?.play()
    }

    private func prepareAudioPlayerIfNeeded() {
        guard audioPlayer == nil else { return }
        guard let data = Data(base64Encoded: Self.bellSoundBase64) else { return }
        do {
            audioPlayer = try AVAudioPlayer(data: data)
            audioPlayer?.prepareToPlay()
        } catch {
            audioPlayer = nil
        }
    }

